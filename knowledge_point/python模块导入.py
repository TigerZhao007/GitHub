# 导入模块
import math
# 现在可以调用模块里包含的函数了
print("求e的n次幂：",math.exp(1))

# from…import 语句：从模块中导入一个指定的部分到当前命名空间中
# 导入模块中的特定函数
from math import exp
# 现在可以直接使用该函数了
print("求e的n次幂：",exp(1))

# from…import* 语句：导入一个模块中的所有项目。然而这种声明不该被过多地使用
from math import *

